/** Match a request pathname to a string literal. We use the URL
 *  parser to avoid string-prefix false positives (e.g. the
 *  "/:id/status" suffix on /api/fleet/trips/:id/status must
 *  not match a hypothetical /api/fleet/trips/:id/status-foo
 *  endpoint). */
function requestMatchesPath(req: Request, path: string): boolean {
  const url = new URL(req.url());
  return url.pathname === path;
}

/** Match a PATCH to the trip status endpoint with a regex
 *  (the :id segment is generated by the server, so we can't
 *  match it literally). */
function requestMatchesTripStatusPatch(req: Request): boolean {
  if (req.method() !== "PATCH") return false;
  const url = new URL(req.url());
  return /^\/api\/fleet\/trips\/[^/]+\/status$/.test(url.pathname);
}

/** Match the cold-chain-compliance GET with a query string.
 *  The route uses a `<select>` for category, so the value is
 *  bounded; we just match the path + a "category=" query
 *  presence. */
function requestMatchesColdChainCompliance(req: Request): boolean {
  if (req.method() !== "GET") return false;
  const url = new URL(req.url());
  if (!/^\/api\/fleet\/vehicles\/[^/]+\/cold-chain-compliance$/.test(url.pathname)) {
    return false;
  }
  return url.searchParams.has("category");
}

/** Route the seven fleet POST/PATCH + 3 special GET endpoints
 *  to stable mock payloads. The seven list GETs (vehicles,
 *  drivers, trips, fuel-logs, repairs, tires, cold-chain) and
 *  the two analytics GETs (fuel-efficiency, maintenance-
 *  backlog) pass through to the live backend unchanged — by
 *  the time my branch lands on main (last in merge order),
 *  worker 1 (fl-server) will have added the 7 missing list
 *  GETs to server/app.js, so the e2e will exercise the real
 *  list endpoints too. */
function installFleetApiMocks(route: Route): void {
  if (requestMatchesPath(route.request(), "/api/fleet/vehicles")) {
    if (route.request().method() === "POST") {
      route.fulfill({
        status: 200,
        contentType: "application/json",
        body: JSON.stringify({ ok: true, vehicle: { id: VEHICLE_ID } }),
      });
      return;
    }
  }
  if (requestMatchesPath(route.request(), "/api/fleet/drivers")) {
    if (route.request().method() === "POST") {
      route.fulfill({
        status: 200,
        contentType: "application/json",
        body: JSON.stringify({ ok: true, driver: { id: DRIVER_ID } }),
      });
      return;
    }
  }
  if (requestMatchesPath(route.request(), "/api/fleet/trips")) {
    if (route.request().method() === "POST") {
      route.fulfill({
        status: 200,
        contentType: "application/json",
        body: JSON.stringify({ ok: true, trip: { id: TRIP_ID } }),
      });
      return;
    }
  }
  if (requestMatchesTripStatusPatch(route.request())) {
    route.fulfill({
      status: 200,
      contentType: "application/json",
      body: JSON.stringify({ ok: true, trip: { id: TRIP_ID, status: "in_transit" } }),
    });
    return;
  }
  if (requestMatchesPath(route.request(), "/api/fleet/fuel-logs")) {
    if (route.request().method() === "POST") {
      route.fulfill({
        status: 200,
        contentType: "application/json",
        body: JSON.stringify({ ok: true, log: { id: FUEL_ID } }),
      });
      return;
    }
  }
  if (requestMatchesPath(route.request(), "/api/fleet/repairs")) {
    if (route.request().method() === "POST") {
      route.fulfill({
        status: 200,
        contentType: "application/json",
        body: JSON.stringify({ ok: true, repair: { id: REPAIR_ID } }),
      });
      return;
    }
  }
  if (requestMatchesPath(route.request(), "/api/fleet/tires/install")) {
    route.fulfill({
      status: 200,
      contentType: "application/json",
      body: JSON.stringify({ ok: true, tire: { id: TIRE_ID } }),
    });
    return;
  }
  if (requestMatchesColdChainCompliance(route.request())) {
    route.fulfill({
      status: 200,
      contentType: "application/json",
      body: JSON.stringify({
        ok: true,
        category: "dairy",
        report: {
          worstTempC: 6.4,
          sustainedMinutes: 28,
          breaches: [
            { startedAt: "2026-06-12T08:14:00Z", endedAt: "2026-06-12T08:42:00Z", minutes: 28 },
          ],
        },
      }),
    });
    return;
  }
  // Anything else under /api/fleet passes through to the live
  // backend (so the e2e can still observe a missing-route
  // regression if the Fastify handler disappears).
  route.continue();
}

/* ────────── page shell + tab switch ────────── */

test.describe("Fleet — Phase 8.6 Pattern A skeleton", () => {
  test("loads, renders the H2 + 7 tabs, defaults to Vehicles, and points back to /app", async ({
    browser,
    request,
    page,
  }) => {
    await page.route("**/api/fleet/**", installFleetApiMocks);

    const ctx = await authedPage(browser, request);
    try {
      const response = await ctx.page.goto("/app/fleet");
      expect(
        response,
        `expected /app/fleet to respond (got ${response?.status()})`,
      ).not.toBeNull();
      expect([200, 304]).toContain(response!.status());

      await waitForHydration(ctx.page);

      // H2 — the Armenian title. The route renders "Ավտոպարկ"
      // (lit. "Vehicle Park" / "Fleet", the legacy H2 verbatim)
      // so the e2e is stable across the migration.
      const panel = ctx.page.getByTestId("fleet-panel");
      await expect(panel).toBeVisible();
      const title = ctx.page.getByTestId("fleet-title");
      await expect(title).toBeVisible();
      expect(title.textContent ?? "").toMatch(/Ավտոպարկ/);

      // English subtitle (bilingual header — Armenian H2 + English <p>).
      const subtitle = ctx.page.getByTestId("fleet-subtitle");
      await expect(subtitle).toBeVisible();
      expect(subtitle.textContent ?? "").toContain("Fleet");

      // 7 tab buttons render in route-local order.
      for (const t of FLEET_TABS) {
        const btn = ctx.page.getByTestId(`fleet-tab-${t}`);
        await expect(btn).toBeVisible();
      }

      // Default tab is Vehicles — the route's initial state.
      const vehicles = ctx.page.getByTestId("fleet-tab-vehicles");
      expect(await vehicles.getAttribute("data-active")).toBe("true");
      await expect(
        ctx.page.getByTestId("fleet-vehicles-form"),
      ).toBeVisible();

      // Click each tab — the matching form appears, the
      // previously-active form unmounts.
      for (const t of FLEET_TABS) {
        await ctx.page.getByTestId(`fleet-tab-${t}`).click();
        expect(
          await ctx.page.getByTestId(`fleet-tab-${t}`).getAttribute("data-active"),
        ).toBe("true");
        await expect(ctx.page.getByTestId(`fleet-${t}-form`)).toBeVisible();
      }

      // Back link — every Pattern A app has a ChevronLeft link
      // pointing to /app (the Today hub). The legacy module
      // used "← back to Today" so the visible label is the
      // most stable assertion.
      const back = ctx.page.getByRole("link", { name: /back to Today/i });
      await expect(back).toBeVisible();
      await expect(back).toHaveAttribute("href", "/app");
    } finally {
      await ctx.page.context().close();
    }
  });
});

