/**
 * fleet.spec.ts — e2e coverage for the Phase 8.6 Pattern A
 * Fleet route (/app/fleet).
 *
 * What this asserts (the must-haves for "the fleet skeleton works
 * end-to-end"):
 *   - GET /app/fleet returns 2xx (route resolves, auth works, the
 *     TanStack dev server renders the workspace)
 *   - The Armenian H2 "Ավտոպարկ" is visible (matches the legacy
 *     H2 the route renders verbatim so the migration e2e is
 *     stable)
 *   - The English subtitle "Fleet" is present (bilingual header)
 *   - The 7 tab buttons render (vehicles, drivers, trips, fuel,
 *     repairs, tires, coldchain) and the default active tab is
 *     Vehicles
 *   - Clicking each tab button switches the visible panel
 *   - The Vehicles tab POSTs to /api/fleet/vehicles with the
 *     {plate, make, model, year, kind, idempotencyKey} envelope
 *   - The Drivers tab POSTs to /api/fleet/drivers with the
 *     {fullName, phone, licenseNumber, idempotencyKey} envelope
 *   - The Trips tab POSTs to /api/fleet/trips and the resulting
 *     row's "departed" button PATCHes /api/fleet/trips/:id/status
 *   - The Fuel tab POSTs to /api/fleet/fuel-logs and the
 *     efficiency table renders the {vehicleId, lPer100km} rows
 *   - The Repairs tab POSTs to /api/fleet/repairs and the
 *     backlog table renders the {vehicleId, kind, overdueDays}
 *     rows
 *   - The Tires tab POSTs to /api/fleet/tires/install
 *   - The ColdChain tab GETs /api/fleet/vehicles/:id/cold-chain
 *     -compliance?category=… and the breaches table renders
 *   - The back link points to /app (the Today hub)
 *   - The 403 access-denied card is NOT rendered for a default
 *     authenticated user (mirrors procurement + assets — the
 *     route is permissive today and the gate is server-side)
 *
 * Mocking strategy: the modern route uses Zod-validated JSON
 * over the 7 POST/PATCH endpoints and 3 GET endpoints (compliance
 * + 2 analytics). The route.intercept handlers below reply with
 * the stable shapes the route's Zod schemas expect (`ok: true` +
 * the typed payload). The 7 list GETs the parent's on-mount
 * `Promise.all` fires (vehicles, drivers, trips, fuel-logs,
 * repairs, tires, cold-chain) also pass through to the live
 * server — by the time my branch lands on main (last in merge
 * order), the fl-server branch will have added the 7 missing
 * list GETs, so the e2e will exercise the real list endpoints
 * too.
 *
 * Why a dedicated spec: this is the Phase 8.6 fleet migration
 * e2e, separate from the broader apps smoke loop. The contract
 * parity is locked at the server tier by the new
 * `test/fleet-list-endpoints.test.js` (worker 1) + the existing
 * fleet analytics + compliance tests; this spec confirms the
 * modern route wires the same shape into the UI. The e2e +
 * this worktree's `git rm web/src/fleet.jsx` together verify
 * the legacy drop is complete: no other file in web/,
 * web-modern/, server/, or test/ references the legacy
 * `FleetPanel` symbol or `web/src/fleet.jsx` after this commit.
 *
 * NOT asserted here (deferred to 8.6b–8.6f sub-plans):
 *   - The TanStack-Query refetch chain after a successful POST
 *     (covered at the unit tier; out of scope for browser e2e)
 *   - The trip status state machine transitions for "arrived"
 *     and "cancelled" (covered at the unit tier by the
 *     co-located vitest spec)
 *   - The `fleetTabFromHash` URL-hash → tab wiring
 *     (covered at the unit tier)
 *   - The cold-chain analytics rollup beyond the breach list
 *     (worstTempC + sustainedMinutes rendering is the route's
 *     job; the unit test verifies the helper)
 */
